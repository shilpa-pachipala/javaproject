package Operators;

public class JumpingCommands {
	
	public static void main(String[] args)
	{
		for (int a=1;a<=10;a++)
		{
			if(a==8)  //prints 1 to7. from 8 it will exit loop and rest dnt print 
			{
				break;
			}
			System.out.println(a);
		}
	
	for (int a1=2;a1<=12;a1++)
	{
		if (a1==7)
		{
			break;
		}
		System.out.println(a1);
	}

	}
	
}
