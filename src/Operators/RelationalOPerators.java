package Operators;

public class RelationalOPerators {
	public static void main(String [] args) {
		
		int a =20;
		int b=30;
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a>b);
		System.out.println(a<b);
		
	}
//relational operators always returns boolean values either true or false
}
 