package Operators;

public class ConditionalStatements {
	public static void main(String [] args)
	{
		int age=18;
		if (age<=18)
		{
			System.out.println("Eligible for vote");
		}
		else
		{
		
			System.out.println("Age is not applicable for vote");
		}
		
		int age1=5;
		if(age1<=5)
		{
			System.out.println("Allowed to school");
		}
		else
		{
			System.out.println("Not Allowed to school");
		}
		
	}

}
